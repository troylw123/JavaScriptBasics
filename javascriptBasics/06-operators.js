// Arithmetic Operators
console.log(36 + 6); // Addition (+)
console.log(67 - 25); // Subtraction (-)
console.log(7 * 9); // Multiplication (*)
console.log(444 / 13); // Division (/)
console.log(2 ** 3); // Exponential (**)
console.log(89 % 5); // Modulus/Remainder (%)

